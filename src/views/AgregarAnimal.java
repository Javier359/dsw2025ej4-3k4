    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       /*agregar animales*/ 
       int edad = Integer.parseInt(jTextFieldEdad.getText());
       double peso = Double.parseDouble(jTextFieldPeso.getText());
       String especie = jComboBoxEspecie.getSelectedItem().toString();
       String sector = jComboBoxSector.getSelectedItem().toString();
       String pais = jComboBoxPais.getSelectedItem().toString();
       double valorFijo = Double.parseDouble(jTextFieldValorFIjo.getText());
       
        try {
            Controlador.cargarAnimal(edad, peso, especie, sector, pais, valorFijo);
            JOptionPane.showMessageDialog(this, "Animal agregado coorrectamente.");
        } catch (InvalidPropertiesFormatException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBoxEspecieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxEspecieActionPerformed

        String seleccion = (String)jComboBoxEspecie.getSelectedItem();
        if (seleccion == null){
        }else{
            if(Controlador.CorrespondeValorFijo(seleccion) == true) {
                jTextFieldValorFIjo.setEnabled(true);
            } else {
                jTextFieldValorFIjo.setEnabled(false);
            }
            this.revalidate();
            this.repaint();
        }
    }//GEN-LAST:event_jComboBoxEspecieActionPerformed

