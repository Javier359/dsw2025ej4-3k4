    private void cargarCombos(){
    
        jComboBoxEspecie.removeAllItems();
        for (Especie e : Controlador.getEspecies()) {
            jComboBoxEspecie.addItem(e.getNombre());
        }

        jComboBoxPais.removeAllItems();
        for (Pais p : Controlador.getPaises()) {
            jComboBoxPais.addItem(p.getNombre());
        }

        jComboBoxSector.removeAllItems();
        for (Sector s : Controlador.getSectores()) {
          jComboBoxSector.addItem(String.valueOf(s.getNumero()));
            
        }
}

